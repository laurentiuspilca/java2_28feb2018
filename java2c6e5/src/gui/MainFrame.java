    private DefaultListModel<Card> model;

    public MainFrame() {
        initComponents();
        model = new DefaultListModel<>();
        jList1.setModel(model);
        afisareCarduri();
        
        jButton1.addActionListener(ev -> this.adaugaCard());
        jList1.addMouseListener(new MouseAdapter() {
            
            @Override
            public void mouseClicked(MouseEvent ev) {
                if (ev.getClickCount() == 2) {
                    Card c = jList1.getSelectedValue();
                    new TransactionsFrame(c);
                }
            }
            
        });
        
        setLocationRelativeTo(null);
        setVisible(true);
    }
    
    private void afisareCarduri() {
        List<Card> c = CardService.getInstance().getCards();
        model.clear();
        c.forEach(model::addElement);
    }
    
    private void adaugaCard() {
        Card c = new Card();
        c.setSerie(jTextField1.getText());
        CardService.getInstance().addCard(c);
        afisareCarduri();
    }

   
